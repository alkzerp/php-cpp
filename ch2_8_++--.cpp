#include <iostream>
using namespace std;
int main()
{
	int quantity=20;
	cout<<"\nquantity:"<<quantity<<endl;
	cout<<"====================="<<endl;
	cout<<"\nquantity++:"<<quantity++<<endl;
	cout<<"\nquantity:"<<quantity<<endl;

	cout<<"====================="<<endl;
	cout<<"\n++quantity:"<<++quantity<<endl;
	cout<<"\nquantity:"<<quantity<<endl;

	cout<<"====================="<<endl;
	cout<<"\n--quantity:"<<--quantity<<endl;
	cout<<"\nquantity:"<<quantity<<endl;

	cout<<"====================="<<endl;
	cout<<"\nquantity--:"<<quantity--<<endl;
	cout<<"\nquantity:"<<quantity<<endl;
	cout<<"====================="<<endl;
	return 0;
}
